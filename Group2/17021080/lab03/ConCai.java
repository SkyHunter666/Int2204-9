package Doi_tuong_xung_quanh;

public class ConCai {
    String bo;
    String me;

    public String getBo() {
        return bo;
    }

    public void setBo(String bo) {
        this.bo = bo;
    }

    public String getMe() {
        return me;
    }

    public void setMe(String me) {
        this.me = me;
    }

    public ConCai(String bo, String me) {
        this.bo = bo;
        this.me = me;
    }
}
