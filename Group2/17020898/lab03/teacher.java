package com.company;

public class teacher {
   String name;
   int age;
   public teacher(){
      name = "Viet";
      age = 21;
   }

   public void setAge(int age) {
      this.age = age;
   }

   public int getAge() {
      return age;
   }

   public void setName(String name) {
      this.name = name;
   }

   public String getName() {
      return name;
   }
}
