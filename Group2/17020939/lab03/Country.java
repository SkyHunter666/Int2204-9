public class Country {
    //Các thuộc tính của lớp Country
    private String name;
    private String capital;
    private double area;
    private int population;

    //Các phương thức get/set của lớp Country
    //name
    public String getName() {
        return name;
    }
    public void setName( String n) {
        name = n;
    }
    //capital
    public String getCapital() {
        return capital;
    }
    public void setCapital( String c) {
        capital = c;
    }
    //area
    public double getArea() {
        return area;
    }
    public void setArea( double a) {
        area = a;
    }
    //population
    public int getPopulation() {
        return population;
    }
    public void setPopulaion( int p) {
        population = p;
    }
}
