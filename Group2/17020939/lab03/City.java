public class City {
    //Các thuộc tính của lớp City
    private String name;
    private String country;
    private double area;
    private int population;

    //Các phương thức get/set của lớp City
    //name
    public String getName() {
        return name;
    }
    public void setName( String n) {
        name = n;
    }
    //country
    public String getCountry() {
        return country;
    }
    public void setCountry( String c) {
        country = c;
    }
    //area
    public double getArea() {
        return area;
    }
    public void setArea( double a) {
        area = a;
    }
    //population
    public int getPopulation() {
        return population;
    }
    public void setPopulaion( int p) {
        population = p;
    }
}
