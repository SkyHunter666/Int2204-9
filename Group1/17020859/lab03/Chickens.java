public class Chickens {

    private int Legs;
    private int Wings;
    private int Eggs;

    public int getLegs() {
        return Legs;
    }

    public void setLegs(int legs) {
        Legs = legs;
    }

    public int getWings() {
        return Wings;
    }

    public void setWings(int wings) {
        Wings = wings;
    }

    public int getEggs() {
        return Eggs;
    }

    public void setEggs(int eggs) {
        Eggs = eggs;
    }
}
