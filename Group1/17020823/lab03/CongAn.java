public class CongAn {
    private String HoTen;
    private String ChucVu;
    private int Tuoi;
    private String QuanHam;
    private String DonVi;

    public String getHoTen() {// ham tra ve ho ten cong an
        return HoTen;
    }

    public void setHoTen(String hoTen) {//ham cai dat ho ten
        HoTen = hoTen;
    }

    public String getChucVu() {// ham tra ve chuc vu hien tai
        return ChucVu;
    }

    public void setChucVu(String chucVu) {//ham cai dat chuc vu hien tai
        ChucVu = chucVu;
    }

    public int getTuoi() {//ham tra ve tuoi
        return Tuoi;
    }

    public void setTuoi(int tuoi) {//ham cai dat tuoi
        Tuoi = tuoi;
    }

    public String getQuanHam() {//ham tra ve quan ham hien tai
        return QuanHam;
    }

    public void setQuanHam(String quanHam) {//ham cai dat quan ham hien tai
        QuanHam = quanHam;
    }

    public String getDonVi() {//ham tra ve don vi cong tac
        return DonVi;
    }

    public void setDonVi(String donVi) {//ham cai dat don vi cong tac
        DonVi = donVi;
    }
}
