public class MayTinh {
    private String Hangsx;
    private String NgaySx;
    private int Gia;
    private String Mau;
    private String ThongSo;

    public String getHangsx() {// ham tra ve ten cua hang san xuat
        return Hangsx;
    }

    public void setHangsx(String hangsx) {// ham cai dat ten hang san xuat
        Hangsx = hangsx;
    }

    public String getNgaySx() {// ham tra ve ngay san xuat
        return NgaySx;
    }

    public void setNgaySx(String ngaySx) {// ham cai dat ngay san xuat
        NgaySx = ngaySx;
    }

    public int getGia() {// ham tra ve gia ban cua may tinh
        return Gia;
    }

    public void setGia(int gia) {// ham cai dat gia ban cua may tinh
        Gia = gia;
    }

    public String getMau() {// ham tra ve mau sac cua may tinh
        return Mau;
    }

    public void setMau(String mau) {// ham cai dat mau sac may tinh
        Mau = mau;
    }

    public String getThongSo() {// ham tra ve thong so cua may tinh
        return ThongSo;
    }

    public void setThongSo(String thongSo) {// ham cai dat thong so may tinh
        ThongSo = thongSo;
    }
}
