public class NgoiNha {
    private int SoTang;
    private String MauSon;
    private String DiaChi;
    private String TenChNha;

    public int getSoTang() {// ham tra ve so tang cua ngoi nha
        return SoTang;
    }

    public void setSoTang(int soTang) {// ham cai dat so tang ngoi nha
        SoTang = soTang;
    }

    public String getMauSon() {// ham tra ve mau son ngoi nha
        return MauSon;
    }

    public void setMauSon(String mauSon) {// ham cai dat mau son ngoi nha
        MauSon = mauSon;
    }

    public String getDiaChi() {// ham tra ve dia chi ngoi nha
        return DiaChi;
    }

    public void setDiaChi(String diaChi) {// ham cai dat dia chi ngoi nha
        DiaChi = diaChi;
    }

    public String getTenChNha() {//ham tra ve ten chu nha
        return TenChNha;
    }

    public void setTenChNha(String tenChNha) {//ham cai dat ten chu nha
        TenChNha = tenChNha;
    }
}
