public class car {
    private String name , tendangxe ;
    private int sochongoi , maxspeed ;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getTendangxe() {
        return tendangxe;
    }

    public void setTendangxe(String tendangxe) {
        this.tendangxe = tendangxe;
    }

    public int getSochongoi() {
        return sochongoi;
    }

    public void setSochongoi(int sochongoi) {
        this.sochongoi = sochongoi;
    }

    public int getMaxspeed() {
        return maxspeed;
    }

    public void setMaxspeed(int maxspeed) {
        this.maxspeed = maxspeed;
    }
}
